let name = "akif";
let age = 21;

console.log("Series of True.");

console.log(name === "akif");
console.log(age === 21);
console.log(name === "akif" || age === 21);
console.log(typeof name === "string" && typeof age === "number");
console.log(age > 20);
console.log(age < 25);

console.log("Series of False.");
console.log(name !== "akif");
console.log(age !== 21);
console.log(name === "farhan" || age === 25);
console.log(typeof name === "number" && typeof age === "string");
console.log(!age > 20);
console.log(age < 21);
